export const getInitials = (name?: string): string => {
  if (!name) return 'S'
  return name
    .split(' ')
    .map(n => n[0])
    .join('')
    .toUpperCase()
    .slice(0, 2)
}

export const formatFullName = (
  firstName: string,
  lastName: string,
  middleName?: string
): string => {
  const capitalize = (str: string) =>
    str.trim().charAt(0).toUpperCase() + str.trim().slice(1).toLowerCase()

  const first = capitalize(firstName)
  const last = capitalize(lastName)
  const middle = middleName?.trim()

  if (middle) {
    return `${first} ${capitalize(middle)} ${last}`
  }
  return `${first} ${last}`
}

export const normalizeClass = (className: string): string => {
  const upper = className.toUpperCase()
  if (upper.includes('JSS') && upper.includes('1')) return 'JSS 1'
  if (upper.includes('JSS') && upper.includes('2')) return 'JSS 2'
  if (upper.includes('JSS') && upper.includes('3')) return 'JSS 3'
  if (upper.includes('SS') && upper.includes('1')) return 'SS 1'
  if (upper.includes('SS') && upper.includes('2')) return 'SS 2'
  if (upper.includes('SS') && upper.includes('3')) return 'SS 3'
  return className
}


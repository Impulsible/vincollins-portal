// ============================================
// CSV PARSING
// ============================================

export const parseCSV = (text: string): BulkUploadRow[] => {
  const lines = text.split('\n').filter(line => line.trim())
  if (lines.length === 0) return []

  const headers = lines[0].split(',').map(h => h.trim().toLowerCase())
  const requiredHeaders = ['first_name', 'last_name', 'class']

  const missingHeaders = requiredHeaders.filter(h => !headers.includes(h))
  if (missingHeaders.length > 0) {
    throw new Error(`Missing required columns: ${missingHeaders.join(', ')}`)
  }

  return lines
    .slice(1)
    .map((line, index) => {
      const values = line.split(',').map(v => v.trim())
      const row: any = { _line: index + 2 }
      headers.forEach((header, colIndex) => {
        row[header] = values[colIndex] || ''
      })
      return row as BulkUploadRow
    })
    .filter(row => row.first_name && row.last_name && row.class)
}

// ============================================
// CSV TEMPLATE GENERATION
// ============================================

export const generateCSVTemplate = (): string => {
  const { CSV_HEADERS, CSV_SAMPLE_ROWS } = require('../constants')
  const rows = [CSV_HEADERS.join(','), ...CSV_SAMPLE_ROWS.map((row: any[]) => row.join(','))]
  return rows.join('\n')
}

export const downloadCSVTemplate = () => {
  const csvContent = generateCSVTemplate()
  const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' })
  const url = window.URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = 'student_bulk_upload_template.csv'
  a.click()
  window.URL.revokeObjectURL(url)
}
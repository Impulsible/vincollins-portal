export const generateEmailPreview = (firstName: string, lastName: string): string => {
  if (!firstName || !lastName) return 'student@vincollins.edu.ng'
  const clean = (str: string) => str.toLowerCase().replace(/[^a-z]/g, '')
  return `${clean(firstName)}.${clean(lastName)}@vincollins.edu.ng`
}

